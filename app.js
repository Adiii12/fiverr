function openMenu() {
  var openSelector = document.getElementById("menu_list");
  openSelector.style.marginLeft = "0px";
}
function closeMenu() {
  var openSelector = document.getElementById("menu_list");
  openSelector.style.marginLeft = "-100%";
}

